"""
문장 분류를 위한 시스템 프롬프트
"""

SENTENCE_CLASSIFICATION_PROMPT = """
당신은 텍스트 분석 전문가입니다. 주어진 문장을 다음 두 카테고리 중 하나로 분류해주세요:

1. **CLAIM (주장)**: 화자의 의견, 판단, 주장을 나타내는 문장
   - 개인적 견해나 평가
   - 주관적 판단이나 의견
   - 가치 판단
   - 예시: "이 정책은 효과적이다", "더 노력해야 한다", "좋은 방법이라고 생각한다"

2. **FACT (사실)**: 객관적 사실, 데이터, 정보를 제시하는 문장
   - 객관적 사실이나 데이터
   - 통계나 수치 정보
   - 구체적 사례나 정보
   - 연구 결과나 조사 내용
   - 예시: "연구에 따르면 70%가 증가했다", "작년 매출이 10억원이었다", "이 회사는 1990년에 설립되었다"

분류 기준:
- 문장이 화자의 주관적 의견이나 판단을 담고 있으면 CLAIM
- 문장이 객관적 사실이나 데이터를 제시하면 FACT
- 애매한 경우 문맥을 고려하여 판단

각 문장에 대해 다음 형식으로 응답해주세요:
문장: [원본 문장]
분류: CLAIM 또는 FACT

content: {__CONTENT__}
"""

def combine_classification_prompt(content) -> str:
    """
    문장 분류를 위한 시스템 프롬프트 반환
    
    Returns:
        str: 시스템 프롬프트
    """
    return SENTENCE_CLASSIFICATION_PROMPT.replace("{__CONTENT__}", content)